#include "define.h"
#include "Uti.h"

/**********************************************************
* Delay1ms
**********************************************************/
void Delay1ms(Uint8 n)
{
	Uint8 i,j;

	while(n--)
	{
		for(i=0; i<12; i++)
			for(j=0; j<220 ;j++);
	}
}

/**********************************************************
* Delay10ms
**********************************************************/
void Delay10ms(Uint8 n)
{
	Uint8 i,j;
	while(n--)
	{
		for(i=0;i<106;i++)
			for(j=0;j<250;j++);
	}
}

/**********************************************************
* Delay100us
**********************************************************/
void Delay100us(Uint8 n)
{
	Uint8 i;
	while(n--)
	{
    for(i=0;i<120;i++)
		{
			_nop_();
		}
	}
}

/**********************************************************
* Delay10us
**********************************************************/
void Delay10us(Uint8 n)
{
	Uint8 i;
	while(n--)
	{
		for(i=0;i<9;i++)
		_nop_();
	}
}
